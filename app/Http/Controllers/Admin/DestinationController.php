    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'title' => 'required',
            'shortDesc' => 'required',
            'meta_title' => 'nullable',
            'meta_description' => 'nullable',
            'meta_h1' => 'nullable',
            'menu_anchor' => 'nullable',
            'desc' => 'nullable',
            'thumbnail' => 'nullable',
        ]);
        $data = $request->all();
        $item = Destination::find($id);
        if ($file = Destination::uploadImage($request, $item->thumbnail)) {
            $data['thumbnail'] = $file;
        }
        $item->update($data);
        return redirect()->route('destinations.index')->with('success', 'Complete');
    }

